#include "minitalk.h"

void	ft_putnbr(int n)
{
	char	i;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		n = n * (-1);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		i = n + '0';
		write(1, &i, 1);
	}
}
